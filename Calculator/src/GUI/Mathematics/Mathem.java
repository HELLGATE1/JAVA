package GUI.Mathematics;

public class Mathem {
    public static double summ (double a,double b){
      return a+b;
    }
       public static double dif (double a,double b) {
        return a-b;
       }
       public static double mul(double a,double b){
        return a*b;
       }
       public static double del(double a,double b){
           return a/b;
       }

}
